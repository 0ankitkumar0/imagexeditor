// Helper to build a Google Fonts CSS URL for a single font
export function getGoogleFontUrl(fontName: string): string {
  const encoded = fontName.replace(/ /g, "+");
  return `https://fonts.googleapis.com/css2?family=${encoded}:ital,wght@0,100;0,200;0,300;0,400;0,500;0,600;0,700;0,800;0,900;1,400;1,700&display=swap`;
}

// Dynamically load a single Google Font by injecting a <link> tag
const loadedFonts = new Set<string>();

export function loadGoogleFont(fontName: string): Promise<void> {
  if (loadedFonts.has(fontName)) return Promise.resolve();

  return new Promise((resolve) => {
    const link = document.createElement("link");
    link.rel = "stylesheet";
    link.href = getGoogleFontUrl(fontName);
    link.onload = () => {
      loadedFonts.add(fontName);
      resolve();
    };
    link.onerror = () => {
      // Still mark loaded to avoid retrying broken fonts
      loadedFonts.add(fontName);
      resolve();
    };
    document.head.appendChild(link);
  });
}
